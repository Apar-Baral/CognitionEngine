from __future__ import annotations

from pathlib import Path

CLAUDE_MARKER = "<!-- cognition-engine-bootstrap -->"


def install_claude_code(root: Path, bootstrap_markdown: str) -> list[str]:
    """Sync bootstrap into CLAUDE.md (append or replace marked section)."""
    cog = root / ".cognition"
    cog.mkdir(parents=True, exist_ok=True)
    (cog / "bootstrap.md").write_text(bootstrap_markdown, encoding="utf-8")

    claude_path = root / "CLAUDE.md"
    section = (
        f"\n{CLAUDE_MARKER}\n"
        f"## Session bootstrap (auto-generated by Cognition Engine)\n\n"
        f"Read `.cognition/bootstrap.md` at session start.\n\n"
        f"```markdown\n{bootstrap_markdown[:4000]}\n```\n"
    )

    if claude_path.is_file():
        content = claude_path.read_text(encoding="utf-8")
        if CLAUDE_MARKER in content:
            before = content.split(CLAUDE_MARKER)[0].rstrip()
            claude_path.write_text(before + section, encoding="utf-8")
        else:
            claude_path.write_text(content.rstrip() + section, encoding="utf-8")
    else:
        claude_path.write_text(
            f"# Project context\n{section}",
            encoding="utf-8",
        )

    return [str(cog / "bootstrap.md"), str(claude_path)]
